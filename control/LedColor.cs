namespace control
{
    public class LedColor
    {
        public int r { get; set; }
        public int g { get; set; }
        public int b { get; set; }

        public LedColor(int r, int g, int b)
        {
            this.r = r;
            this.g = g;
            this.b = b;
        }
    }
}
